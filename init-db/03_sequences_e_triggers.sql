CREATE SEQUENCE SQ_ESP NOCACHE;
CREATE SEQUENCE SQ_GAI NOCACHE;
CREATE SEQUENCE SQ_MUT NOCACHE;
CREATE SEQUENCE SQ_STT NOCACHE;
CREATE SEQUENCE SQ_PAS NOCACHE;

CREATE TRIGGER TG_SQ_ESP
BEFORE INSERT
ON ESPECIES
FOR EACH ROW
BEGIN
    :NEW.esp_id := SQ_ESP.NEXTVAL;
END;

