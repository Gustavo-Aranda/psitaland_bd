CREATE OR REPLACE TRIGGER TG_HESPECIES
BEFORE UPDATE OR DELETE ON ESPECIES
FOR EACH ROW
BEGIN
    INSERT INTO HESPECIES (
        ESP_ID,
        ESP_NOME,
        HESP_DT_INCLUSAO
    ) VALUES (
        :OLD.ESP_ID,
        :OLD.ESP_NOME,
        SYSDATE
    );
END;
/

